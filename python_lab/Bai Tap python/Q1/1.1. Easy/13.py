# Create a list of numbers and remove all duplicates.


# Tạo danh sách các số
numbers = [1, 2, 3, 2, 4, 5, 5, 6, 3, 7]

# Loại bỏ các phần tử trùng lặp
unique_numbers = list(set(numbers))

# Hiển thị kết quả
print("Danh sách ban đầu:", numbers)
print("Danh sách sau khi loại bỏ trùng lặp:", unique_numbers)
