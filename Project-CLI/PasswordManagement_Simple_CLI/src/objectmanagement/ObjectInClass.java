package objectmanagement;

public class ObjectInClass {
    private int displayMenuSize;
    private int choiceNumber;
    private int choiceMaxNumber;
    private int len;

    public int getDisplayMenuSize() {
        return displayMenuSize;
    }

    public void setDisplayMenuSize(int displayMenuSize) {
        this.displayMenuSize = displayMenuSize;
    }

    public int getChoiceNumber() {
        return choiceNumber;
    }

    public void setChoiceNumber(int choiceNumber) {
        this.choiceNumber = choiceNumber;
    }

    public int getChoiceMaxNumber() {
        return choiceMaxNumber;
    }

    public void setChoiceMaxNumber(int choiceMaxNumber) {
        this.choiceMaxNumber = choiceMaxNumber;
    }

    public int getLen() {
        return len;
    }

    public void setLen(int len) {
        this.len = len;
    }
}
